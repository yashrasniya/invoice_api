class Point:

    def __init__(self,title,x=None,y=None,label=None,value=None,**kwargs):
        self.title=title
        try: self.x = float(x) if x is not None else 0.0
        except: self.x = 0.0
        try: self.y = float(y) if y is not None else 0.0
        except: self.y = 0.0
        self.value=value
        self.label=label
        self.raw=kwargs
        self.next_line=kwargs.get('next_line',None)
        
        fs = kwargs.get('font_size')
        try: self.font_size = float(fs) if fs is not None else None
        except: self.font_size = None
        
        self.font=kwargs.get('font',None)
        
        lim = kwargs.get('limit')
        try: self.limit = int(lim) if lim is not None else None
        except: self.limit = None
        
        nl = kwargs.get('no_lines', 100)
        try: self.no_lines = int(nl)
        except: self.no_lines = 100
        self.suffix=kwargs.get('suffix',None)
        self.prefix=kwargs.get('prefix',None)
        self.autoGenerated=kwargs.get('autoGenerated',False)

    def add_suffix(self):
        if self.suffix is not None:
            self.value=str(self.value)+self.suffix
    def type(self):
        return "Point"
    def go_down(self,d):
        self.y -= d

    def add_prefix(self):
        if self.prefix:
            self.value = self.prefix  + str(self.value)

    def type(self):
        return "Point"

    def __str__(self):
        return self.title
class Polygon:
    def __init__(self, title, points=None, **kwargs):
        """
        :param title: Name/identifier of the polygon
        :param points: List of (x, y) tuples [(x1, y1), (x2, y2), ...]
        :param kwargs: Extra attributes like stroke, fill, line_width, etc.
        """
        self.title = title
        self.points = points if points is not None else []
        self.raw = kwargs
        self.label =kwargs.get("label",'')

        # Style/extra attributes
        self.stroke = kwargs.get("stroke_color", "black")   # outline color
        self.fill = kwargs.get("fill", None)         # fill color (None = transparent)
        self.src = kwargs.get("src", None)         # fill color (None = transparent)
        self.rectangles_type = kwargs.get("rectangles_type", None)         # fill color (None = transparent)
        
        lw = kwargs.get("line_width", 1)
        try: self.line_width = float(lw)
        except: self.line_width = 1.0
        
        self.closed = kwargs.get("closed", True)     # auto-close polygon

    def add_point(self, x, y):
        """Add a new point (x,y) to polygon"""
        self.points.append((x, y))

    def type(self):
        return "Polygon"

    def get_bounds(self):
        """Return bounding box (min_x, min_y, max_x, max_y)"""
        if not self.points:
            return None
        xs, ys = zip(*self.points)
        return min(xs), min(ys), max(xs), max(ys)

    def go_down(self,d):
        points = []
        for i,y in enumerate(self.points):
            y = self.points[i][1] - d
            x = self.points[i][0]
            points.append((x,y))
        self.points = points
    def __str__(self):
        return f"Polygon({self.title}, points={len(self.points)})"
